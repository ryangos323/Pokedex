DROP DATABASE IF EXISTS pokemon;
CREATE DATABASE pokemon;
USE pokemon;

CREATE TABLE pokemon (
  p_id        INT            PRIMARY KEY   AUTO_INCREMENT,
  p_index      INT(5)   NOT NULL,
  p_name      VARCHAR(50)   NOT NULL      UNIQUE,
  p_wieght      DECIMAL(5,1)   NOT NULL,
  p_height_ft      INT(5)   NOT NULL,
  p_height_in      INT(5)   NOT NULL,
  p_gender      VARCHAR(20)   NOT NULL,
  p_type      VARCHAR(50)   NOT NULL,
  p_weakness      VARCHAR(50)   NOT NULL,
  p_hp      INT(10)   NOT NULL,
  p_attack      INT(10)   NOT NULL,
  p_defense      INT(10)   NOT NULL,
  p_speed      INT(10)   NOT NULL
);

CREATE TABLE custom_pokemon (
  cp_id        INT            PRIMARY KEY   AUTO_INCREMENT,
  cp_name      VARCHAR(50)   NOT NULL,
  cp_wieght      DECIMAL(5,1)   NOT NULL,
  cp_height_ft      INT(5)   NOT NULL,
  cp_height_in      INT(5)   NOT NULL,
  cp_gender      VARCHAR(20)   NOT NULL,
  cp_type      VARCHAR(50)   NOT NULL,
  cp_weakness      VARCHAR(50)   NOT NULL,
  cp_hp      INT(10)   NOT NULL,
  cp_attack      INT(10)   NOT NULL,
  cp_defense      INT(10)   NOT NULL,
  cp_speed      INT(10)   NOT NULL
);

